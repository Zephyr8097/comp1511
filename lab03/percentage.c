#include<stdio.h>
int main()
{
    double total_marks, marks_awarded;
    double percentage;
    printf("Enter the total number of marks in the exam: ");
    scanf("%lf", &total_marks);
    printf("Enter the number of marks the student was awarded: ");
    scanf("%lf", &marks_awarded);
    percentage = (marks_awarded / total_marks) * 100;
    printf("The student scored %.0lf%% in this exam.\n", percentage);
    return 0;
}
